package com.esrisy.WebQuery.common;

public class QueryResult {
	
	private String id;
	private String title;
	private String author;
	private String country;
	private String newsSource;
	private String publishDate;
	private String qPublishdate;
	private String qPublishDataMonth;
	private String newsContent;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getNewsSource() {
		return newsSource;
	}
	public void setNewsSource(String newsSource) {
		this.newsSource = newsSource;
	}
	public String getPublishDate() {
		return publishDate;
	}
	public void setPublishDate(String publishDate) {
		this.publishDate = publishDate;
	}
	public String getqPublishdate() {
		return qPublishdate;
	}
	public void setqPublishdate(String qPublishdate) {
		this.qPublishdate = qPublishdate;
	}
	public String getqPublishDataMonth() {
		return qPublishDataMonth;
	}
	public void setqPublishDataMonth(String qPublishDataMonth) {
		this.qPublishDataMonth = qPublishDataMonth;
	}
	public String getNewsContent() {
		return newsContent;
	}
	public void setNewsContent(String newsContent) {
		this.newsContent = newsContent;
	}
}
